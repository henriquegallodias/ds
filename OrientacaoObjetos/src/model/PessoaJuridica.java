package model;

public class PessoaJuridica extends Pessoa {
	private String rsocial;
	private double cnpj;
	
	public String getRsocial() {
		return rsocial;
	}
	public void setRsocial(String rsocial) {
		this.rsocial = rsocial;
	}
	public double getCnpj() {
		return cnpj;
	}
	public void setCnpj(double cnpj) {
		this.cnpj = cnpj;
	}
	
	
	

}
